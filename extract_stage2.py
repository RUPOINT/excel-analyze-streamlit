import pandas as pd

def process_excel_stage2(df):
    # Оставляем только нужные колонки, включая новую!
    keep_cols = [
        "ND (Номер декларации)",
        "G141 (ИНН декларанта)",
        "G142 (Наименование декларанта)",
        "G143 (Адрес декларанта)",
        "G202 (Условие поставки)",
        "Перемещение + Режим",               # <-- добавили!
        "Декларант — количество деклараций",
        "Пустые ячейки в колонке брокера",
        "Количество брокеров",
        "Контакты",
    ]
    # Оставляем только существующие (если каких-то нет — не будет ошибки)
    df_new = df[[col for col in keep_cols if col in df.columns]].copy()
    return df_new
